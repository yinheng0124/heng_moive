package com.bw.bean;

public class HouseOwner {

	private Integer  owner_id;
	private String  owner_name;
	private String owner_IdNo;
	private String owner_sex;
	/**
	 * @return the owner_id
	 */
	public Integer getOwner_id() {
		return owner_id;
	}
	/**
	 * @param owner_id the owner_id to set
	 */
	public void setOwner_id(Integer owner_id) {
		this.owner_id = owner_id;
	}
	/**
	 * @return the owner_name
	 */
	public String getOwner_name() {
		return owner_name;
	}
	/**
	 * @param owner_name the owner_name to set
	 */
	public void setOwner_name(String owner_name) {
		this.owner_name = owner_name;
	}
	/**
	 * @return the owner_IdNo
	 */
	public String getOwner_IdNo() {
		return owner_IdNo;
	}
	/**
	 * @param owner_IdNo the owner_IdNo to set
	 */
	public void setOwner_IdNo(String owner_IdNo) {
		this.owner_IdNo = owner_IdNo;
	}
	/**
	 * @return the owner_sex
	 */
	public String getOwner_sex() {
		return owner_sex;
	}
	/**
	 * @param owner_sex the owner_sex to set
	 */
	public void setOwner_sex(String owner_sex) {
		this.owner_sex = owner_sex;
	}
	
	
	
}
